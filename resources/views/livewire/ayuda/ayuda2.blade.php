            <h4><b>Cómo crear un punto de venta asociado a WebServices en la AFIP</b></h4><br>
            <p>Para comenzar a facturar electrónicamente, necesitarás crear un punto de venta WebService en la web de la AFIP. ¡Te mostramos cómo hacerlo!</p>
            <h6>1) Ingresar a la <a style="color: orange; font-size:16px; font-weight:600;" href="https://www.afip.gob.ar/" target="_blank">pagina de AFIP > </a> con tu usuario y contraseña</h6>
            <br>
            <img style="width:70% !important;" src="assets/pos/img/imagenes_facturacion/PASO_1.jpg"  class="card-img-top">
            <br>
            <h6>2) Desde Mis Servicios, hacé clic en "Administración de puntos de venta y domicilios."</h6>
            <br>
            <img style="width:70% !important;" src="assets/pos/img/tutoriales/alta_punto_venta_en_afip/PASO_2.jpg"  class="card-img-top">
            <br>
            <h6>3) Hace click en el nombre de tu empresa</h6>
            <br>
            <img style="width:70% !important;" src="assets/pos/img/tutoriales/alta_punto_venta_en_afip/PASO_3.jpg"  class="card-img-top">
            <br>
            <h6>4) Presiona en A/B/M de puntos de venta. </h6>
            <br>
            <img style="width:70% !important;" src="assets/pos/img/tutoriales/alta_punto_venta_en_afip/PASO_4.jpg"  class="card-img-top">
            <br>
            <h6>5) Hace clic en Agregar para crear tu punto de venta. </h6>
            <br>
            <img style="width:70% !important;" src="assets/pos/img/tutoriales/alta_punto_venta_en_afip/PASO_5.jpg"  class="card-img-top">
            <br>
            <h6>6) Completa los datos que aparecen en la ventana: </h6>
            <br>
            Número de punto de venta: tiene que ser distinto a los que ya hayas creado.<br>
            Nombre de fantasía de tu empresa: podés escribir el que quieras porque solo será visible para AFIP, incluso podés escribir Facturante.<br>
            Dominio asociado: podés dejarlo sin completar o escribir el dominio de tu sitio web o Tienda Online, por ejemplo, mitiendabazar.com.<br>
            Sistema: tenés que elegir una opción dependiendo de cuál sea tu condición tributaria:<br>
            <br><br>
            Si sos MONOTRIBUTISTA elegí Factura Electrónica – Monotributo - WebServices<br>
            Si sos RESPONSABLE INSCRIPTO elegí RECE para aplicativo y WebServices<br>
            Si sos IVA EXENTO elegí Facturación Electrónica - exento en IVA - WebServices<br>
            Domicilio: Seleccioná el que corresponda al punto de venta.<br>
            <br><br>
            Al terminar, presioná Aceptar.  
